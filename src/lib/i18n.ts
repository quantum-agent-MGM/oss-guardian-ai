export function useLanguage() {
  const [lang, setLang] = useState<Lang>("en");

  useEffect(() => {
    // Detect browser language
    const browserLang = navigator.language || "";
    if (browserLang.startsWith("es")) {
      setLang("es");
    }
  }, []);

  return { lang, t: translations[lang] };
}

